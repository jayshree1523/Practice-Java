public class Person
{
  private String Name;
  private String contact_no;
  public Person (String Name, String contact_no)
  {
    this.Name = Name;
    this.contact_no = contact_no;
  }
  public String getName ()
  {
    return Name;
  }
  public String getContact_no ()
  {
    return contact_no;
  }
}
